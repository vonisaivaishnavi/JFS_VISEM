package com.skillnext;

public class Student {
    private String name;
    private int sem;
    private String dept;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getSem() {
        return sem;
    }
    public void setSem(int sem) {
        this.sem = sem;
    }

    public String getDept() {
        return dept;
    }
    public void setDept(String dept) {
        this.dept = dept;
    }
}